public class DataTypes {
    public static void main(String[] args) {
        // primitive data types
        byte age = 30; // byte
        // short salary = 50000 ; // short
        int grade = 85; // int
        long population = 7800000000L; // long
        float pi = 3.14f; // float
        double e = 2.71828; // double
        boolean isEmployed = true; // boolean
        char initial = 'J'; // char

        // non-primitive data types
        String name = "John Doe"; // String

        // Arrays and Classes can also be considered as non-primitive data types, but we
        // will cover them in later sections.

        // printing the values of variables
        System.out.println("Age: " + age);
        // System.out.println("Salary: " + salary);
        System.out.println("Grade: " + grade);
        System.out.println("Population: " + population);
        System.out.println("Pi: " + pi);
        System.out.println("E: " + e);
        System.out.println("Is Employed: " + isEmployed);
        System.out.println("Initial: " + initial);
        System.out.println("Name: " + name);
    }
}
